(() => {
    function initFeatureModules({
        documentRef = document,
        elements = {},
        catalogs = {},
        shared = {},
        utils = {},
        data = {},
        core = {},
    }) {
        const {
            escapeHtml,
            cloneJsonValue,
            linesToArray,
            textToList,
            formatBooleanSelectValue,
            parseBooleanSelectValue,
            formatSearchBarSelectValue,
            parseSearchBarSelectValue,
            stablePreferenceValueKey,
            serializePreferenceValue,
            parsePreferenceValue,
            serializePreferenceSelectValue,
            normalizePreferenceName,
        } = utils;
        const { fromEditorValue, toEditorValue } = data;
        const {
            t,
            setStatus,
            getEditor,
            setCurrentRaw,
            setRenderManualHomeAndSearchSectionStatuses,
        } = shared;
        const {
            wizardPreferenceSections,
            wizardKnownPreferences,
            wizardKnownPreferenceIndex,
            wizardPreferenceEntryManagedKeys,
            searchEnginePresets,
            wizardSearchEngineItemManagedKeys,
            wizardHomepageManagedKeys,
            wizardLandingScalarKeys,
            wizardSearchEnginesManagedKeys,
            wizardFirefoxHomeManagedKeys,
            wizardFirefoxSuggestManagedKeys,
            wizardProxyManagedKeys,
        } = catalogs;
        const {
            wizardSearchEngineAddButtonEl,
            wizardSearchEngineEmptyEl,
            wizardSearchEngineListEl,
            wizardSearchEngineTemplateEl,
            wizardSearchEnginePresetButtons,
            wizardHomepageUrlEl,
            wizardHomepageAdditionalEl,
            wizardHomepageStartPageEl,
            wizardHomepageLockedEl,
            wizardSearchBarEl,
            wizardSearchSuggestEl,
            wizardSearchDefaultEngineEl,
            wizardSearchPreventInstallsEl,
            wizardSearchRemoveEl,
            wizardHomepageSectionStatusEl,
            wizardHomeOverridesSectionStatusEl,
            wizardFirefoxHomeSectionStatusEl,
            wizardFirefoxHomeFineTuningToggleEl,
            wizardFirefoxHomeFineTuningPanelEl,
            wizardSearchDefaultsSectionStatusEl,
            wizardSearchDefaultsPresetButtons,
            wizardSearchDefaultsFineTuningToggleEl,
            wizardSearchDefaultsFineTuningPanelEl,
            wizardFirefoxSuggestSectionStatusEl,
            wizardFirefoxSuggestPresetButtons,
            wizardSearchSuggestFineTuningToggleEl,
            wizardSearchSuggestFineTuningPanelEl,
            wizardNewTabPageEl,
            wizardOverrideFirstRunEl,
            wizardOverridePostUpdateEl,
            wizardFirefoxHomeInputs,
            wizardFirefoxSuggestInputs,
            wizardGeneralPolicySectionStatusEl,
            wizardGeneralPolicyPresetButtons,
            wizardProxyModeEl,
            wizardProxySectionStatusEl,
            wizardProxyLockedEl,
            wizardProxyHttpEl,
            wizardProxySslEl,
            wizardProxyFtpEl,
            wizardProxySocksEl,
            wizardProxySocksVersionEl,
            wizardProxyPassthroughEl,
            wizardProxyAutoConfigUrlEl,
            wizardProxyUseHttpForAllEl,
            wizardProxyAutoLoginEl,
            wizardProxyUseDnsEl,
            wizardProxyModeGroups,
            wizardNetworkEnterpriseSectionStatusEl,
            wizardNetworkEnterpriseFineTuningToggleEl,
            wizardNetworkEnterpriseFineTuningPanelEl,
        } = elements;

        const preferences = window.BPMProfilesPreferences.create({
            documentRef,
            wizardPreferenceSections,
            wizardKnownPreferences,
            wizardKnownPreferenceIndex,
            wizardPreferenceEntryManagedKeys,
            dependencies: {
                t,
                escapeHtml,
                cloneJsonValue,
                stablePreferenceValueKey,
                serializePreferenceValue,
                parsePreferenceValue,
                serializePreferenceSelectValue,
                normalizePreferenceName,
                fromEditorValue,
                toEditorValue,
                setStatus: (...args) => setStatus(...args),
            },
            state: {
                getEditor,
                setCurrentRaw,
            },
        });

        const searchEngines = window.BPMProfilesSearchEngines.create({
            documentRef,
            elements: {
                addButtonEl: wizardSearchEngineAddButtonEl,
                emptyEl: wizardSearchEngineEmptyEl,
                listEl: wizardSearchEngineListEl,
                templateEl: wizardSearchEngineTemplateEl,
                presetButtons: wizardSearchEnginePresetButtons,
            },
            searchEnginePresets,
            wizardSearchEngineItemManagedKeys,
            dependencies: {
                t,
                cloneJsonValue,
                escapeHtml,
                setStatus: (...args) => setStatus(...args),
                applyNetworkFromWizard: () => network.applyFromWizard(),
            },
        });

        const network = window.BPMProfilesNetwork.create({
            documentRef,
            elements: {
                wizardHomepageUrlEl,
                wizardHomepageAdditionalEl,
                wizardHomepageStartPageEl,
                wizardHomepageLockedEl,
                wizardSearchBarEl,
                wizardSearchSuggestEl,
                wizardSearchDefaultEngineEl,
                wizardSearchPreventInstallsEl,
                wizardSearchRemoveEl,
                wizardSearchEngineAddButtonEl,
                wizardSearchEnginePresetButtons,
                wizardHomepageSectionStatusEl,
                wizardHomeOverridesSectionStatusEl,
                wizardFirefoxHomeSectionStatusEl,
                wizardFirefoxHomeFineTuningToggleEl,
                wizardFirefoxHomeFineTuningPanelEl,
                wizardSearchDefaultsSectionStatusEl,
                wizardSearchDefaultsPresetButtons,
                wizardSearchDefaultsFineTuningToggleEl,
                wizardSearchDefaultsFineTuningPanelEl,
                wizardFirefoxSuggestSectionStatusEl,
                wizardFirefoxSuggestPresetButtons,
                wizardSearchSuggestFineTuningToggleEl,
                wizardSearchSuggestFineTuningPanelEl,
                wizardNewTabPageEl,
                wizardOverrideFirstRunEl,
                wizardOverridePostUpdateEl,
                wizardFirefoxHomeInputs,
                wizardFirefoxSuggestInputs,
                wizardGeneralPolicySectionStatusEl,
                wizardGeneralPolicyPresetButtons,
                wizardProxyModeEl,
                wizardProxySectionStatusEl,
                wizardProxyLockedEl,
                wizardProxyHttpEl,
                wizardProxySslEl,
                wizardProxyFtpEl,
                wizardProxySocksEl,
                wizardProxySocksVersionEl,
                wizardProxyPassthroughEl,
                wizardProxyAutoConfigUrlEl,
                wizardProxyUseHttpForAllEl,
                wizardProxyAutoLoginEl,
                wizardProxyUseDnsEl,
                wizardProxyModeGroups,
                wizardNetworkEnterpriseSectionStatusEl,
                wizardNetworkEnterpriseFineTuningToggleEl,
                wizardNetworkEnterpriseFineTuningPanelEl,
            },
            dependencies: {
                t,
                cloneJsonValue,
                linesToArray,
                textToList,
                formatBooleanSelectValue,
                parseBooleanSelectValue,
                formatSearchBarSelectValue,
                parseSearchBarSelectValue,
                fromEditorValue,
                toEditorValue,
                syncBooleanSelectGroup: shared.syncBooleanSelectGroup,
                applyBooleanSelectGroup: shared.applyBooleanSelectGroup,
                buildSearchEngineAddItemsFromWizard: searchEngines.buildAddItemsFromWizard,
                syncSearchEngineDraftsFromPolicy: searchEngines.syncDraftsFromPolicy,
                renderSearchEngineDrafts: searchEngines.renderDrafts,
                consumeSearchEngineSyncOnce: searchEngines.consumePendingSyncOnce,
                clearSearchEngineSyncOnce: searchEngines.clearPendingSyncOnce,
                markSearchEngineSyncOnce: searchEngines.markPendingSyncOnce,
                renderHomeReviewSummary: core.renderHomeReviewSummary,
                renderSearchReviewSummary: core.renderSearchReviewSummary,
                setStatus: (...args) => setStatus(...args),
            },
            state: {
                getEditor,
                setCurrentRaw,
                wizardHomepageManagedKeys,
                wizardLandingScalarKeys,
                wizardSearchEnginesManagedKeys,
                wizardFirefoxHomeManagedKeys,
                wizardFirefoxSuggestManagedKeys,
                wizardProxyManagedKeys,
            },
        });
        setRenderManualHomeAndSearchSectionStatuses(network.renderManualHomeAndSearchSectionStatuses);

        return {
            ...preferences,
            ...searchEngines,
            ...network,
            syncWizardPreferencesFromEditor: preferences.syncWizardPreferencesFromEditor,
            syncWizardNetworkFromEditor: network.syncFromEditor,
            appendSearchEngineDraft: searchEngines.appendDraft,
            applySearchEnginePreset: searchEngines.applyPreset,
            bindSearchEngineAddButtonListeners: searchEngines.bindAddButtonListeners,
            applyNetworkFromWizard: network.applyFromWizard,
            rerenderSearchEngineDraftsForLocale: searchEngines.rerenderForLocale,
        };
    }

    window.BPMProfilesBootstrapFeatures = {
        initFeatureModules,
    };
})();
